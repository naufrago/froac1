    <h2>
        Término RSS (Really Simple Syndication)
    </h2>
    <p>
        <strong>RSS: </strong><br>
        RSS por sus siglas en inglés significa Really Simple Syndication o Rich Site Summary, 
        que en español puede traducirse como “Resúmenes desde un sitio rico en contenidos” o 
        “fácil distribución de contenidos”.
    </p>
    <p>
        <strong>¿Qué es RSS: </strong>
        RSS es una forma muy sencilla para que puedas recibir, directamente en tu ordenador o 
        en una página web online (a través de un lector RSS) información actualizada sobre tus 
        páginas web favoritas, sin necesidad de que tengas que visitarlas una a una. 
        Esta información se actualiza automáticamente, sin que tengas que hacer nada. 
        Para recibir las noticias RSS la página deberá tener disponible el servicio RSS 
        y deberás tener un lector Rss.
    <p>
        Si existen varias páginas web que te interesan que van actualizando sus contenidos y 
        te gustaría mantenerte informado, un lector RSS te ahorrará mucho tiempo en esta tarea. 
        Gracias al RSS, no tendrás que visitar cada una de las páginas web que te interesan para 
        ver si han añadido o no algún artículo que te pueda interesar. Estas páginas te informarán
        a ti (a través de tu lector de RSS). Cuando ingreses a tu Lector RSS (o Rss Reader),
        estarás automáticamente informado sobre todas las novedades que se han producido en
        todas las páginas web que has dado de alta.
    </p>
    <p>
        Los sistemas RSS tienen muchas ventajas que vamos a resumir. Gracias al RSS, tendrás 
        reunidas en un mismo lugar y a un solo clic de distancia, toda la información actualizada 
        de las páginas web (Fuentes o canales RSS) que más te interesan. Si todavía no te han 
        quedado claro lo que es el RSS y lo que te puede aportar, puedes leer información adicional
        sobre para qué sirve el RSS y algunos ejemplos ilustrativos.
    </p>
</p>
